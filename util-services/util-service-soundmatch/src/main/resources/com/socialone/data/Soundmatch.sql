DROP TABLE IF EXISTS socialone.`UTILS_SOUND_MATCHES`;

CREATE TABLE socialone.`UTILS_SOUND_MATCHES` (
  `WORD` varchar(128) CHARACTER SET utf8mb4,
  `PRESENTATION` varchar(128) CHARACTER SET utf8mb4,
  `ALGORITHM` varchar(50) CHARACTER SET utf8mb4,
  PRIMARY KEY (`WORD`,`ALGORITHM`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
